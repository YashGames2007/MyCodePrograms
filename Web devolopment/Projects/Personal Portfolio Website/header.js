var typed_Home = new Typed("#nav_anchor", {
    strings: [],
	typeSpeed: 40,
	backSpeed: 10,
    fadeOut: true,
    backDelay: 1000
});

typed_Home.stop();
var element = document.querySelector("#nav_anchor");
element.addEventListener("mouseenter", function () { typed_Home.start(); });
element.addEventListener("mouseleave", function () { typed_Home.reset(); });

